//class dan nama file
public class No3 {
  // main function
  public static void main(String[] args) {
    // deklarasi variable
    int i = 5;
    // looping
    while (i > 0) {
      int j = 0;
      while (j < i) {
        System.out.print('*');
        j++; // j = j + 1
      }
      System.out.println();
      i--; // i = i-1
    }
  }
}
